    public static event Action<InventoryLocation, List<InventoryItem>> InventoryUpdateEvent;

    public static void CallInventoryUpdateEvent(InventoryLocation inventoryLocation, List<InventoryItem> inventoryItems)
    {
        if (InventoryUpdateEvent != null)
        {
            InventoryUpdateEvent(inventoryLocation, inventoryItems);
        }
    }


    public static event MovementDelegate MovementEvent;

    public static void CallMoventEvent(float inputX, float inputY, bool isWalking, bool isRunning, bool isIdle, bool isCarrying,
    ToolEffect toolEffect,
    bool isUsingToolRight, bool isUsingToolLeft, bool isUsingToolUp, bool isUsingToolDown,
